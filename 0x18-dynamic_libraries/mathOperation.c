/**
 * add - function to call
 *@a: first Number to add.
 *@b: second Number to add.
 * return - sum
*/
int add(int a, int b)
{
	return a + b;
}

/**
 * sub - function to call
 *@a: first Number .
 *@b: second Number.
 * return - sub
*/
int sub(int a, int b)
{
	return a - b;
}

/**
 * mul - function to call
 *@a: first Number .
 *@b: second Number.
 * return - mul
*/
int mul(int a, int b)
{
	return a * b;
}

/**
 * div - function to call
 *@a: first Number .
 *@b: second Number.
 * return - div
*/
int div(int a, int b)
{
	return a / b;
}

/**
 * mod - function to call
 *@a: first Number .
 *@b: second Number.
 * return - mod
*/
int mod(int a, int b)
{
	return a % b;
}